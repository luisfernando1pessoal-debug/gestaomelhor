function slugify(str) {
  return str
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-zA-Z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .toLowerCase();
}

function generatePages() {
  const pages = [];

  for (const niche of NICHES) {
    for (const { city, state } of CITIES) {
      const citySlug = slugify(city);
      const slug = `${niche.baseSlug}-em-${citySlug}`;

      pages.push({
        slug,
        baseSlug: niche.baseSlug,
        nicheLabel: niche.nicheLabel,
        city,
        state,
        title: `${niche.seoPrefix} em ${city} ${state} | Gestão Melhor`,
        description: `${niche.descPrefix} em ${city}, ${state}. Teste grátis por 14 dias. Sem cartão de crédito.`,
      });
    }
  }

  return pages;
}

